int rt5503_calculate_offset(int reg)
{
	int i = 0;
	int offset = 0;
	int ret = -EINVAL;

	for (i = 0; i < REGISTER_NUM; i++,
		offset += (rt5503_regmap[i]->size)) {
		if (rt5503_regmap[i]->addr == reg) {
			ret = offset;
			break;
		}
	}
	return ret;
}
EXPORT_SYMBOL(rt5503_calculate_offset);

int rt5503_calculate_total_size(void)
{
	int i = 0;
	int ret = 0;

	for (i = 0; i < REGISTER_NUM; i++)
		ret += rt5503_regmap[i]->size;
	return ret;
}
EXPORT_SYMBOL(rt5503_calculate_total_size);
